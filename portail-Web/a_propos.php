        <div class="section">
            <h2>Qu'est-ce qu'un trombinoscope ?</h2>
            <p>
                Un trombinoscope est un outil visuel qui consiste en une galerie de portraits de personnes, généralement accompagnés de leurs noms et de leurs fonctions. Il est souvent utilisé dans les contextes professionnels, académiques ou associatifs pour faciliter l'identification et la mémorisation des membres d'un groupe.
            </p>
        </div>
        <div class="section">
            <h2>Utilité du trombinoscope</h2>
            <p>
                Le trombinoscope permet de mieux connaître les membres d'une organisation ou d'une communauté en mettant un visage sur un nom. Cela peut renforcer les liens entre collègues, étudiants ou membres d'une association en rendant les interactions plus personnelles. De plus, dans un cadre professionnel, il aide à identifier rapidement les responsables et les interlocuteurs appropriés, ce qui peut améliorer l'efficacité de la communication interne.
            </p>
        </div>
        <div class="section">
            <h2>Notre projet de trombinoscope</h2>
            <p>
                Notre projet consiste en le développement d'un portail Web sécurisé pour le trombinoscope des promotions de l’ESIITECH. Ce trombinoscope est un outil précieux pour favoriser la convivialité, l'efficacité et pour connaître les visages de nos camarades au sein de l'ESIITEH. Il contribue également à créer un environnement plus humain et collaboratif avec les membres de l'administration.
            </p>
        </div>
